# leitura de um numero inteiro
num = int(input("Inserir numero inteiro: "))
# estrutura condicional (if - then - else)
if num < 0:
    # numero negativo
    print ("Numero inteiro negativo")
elif num == 0: # else if
    print ("Zero")
else:
    # numero positivo (>0)
    print ("Numero inteiro positivo")

